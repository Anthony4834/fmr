async function processMissingZips(filePath: string, count: number) {
  console.log(`\n=== Processing ${count} Missing ZIP Codes ===\n`);

  if (!process.env.POSTGRES_URL) {
    throw new Error('POSTGRES_URL environment variable is required');
  }
  configureDatabase({ connectionString: process.env.POSTGRES_URL });

  // Read file
  const content = readFileSync(filePath, 'utf-8');
  const lines = content.split('\n');
  const header = lines[0];
  const dataLines = lines.slice(1).filter(l => l.trim());

  // Get first N ZIP codes
  const zipsToProcess = dataLines.slice(0, count).map(line => {
    const parts = line.split('\t');
    return parts[0]?.trim();
  }).filter(zip => zip && /^\d{5}$/.test(zip));

  console.log(`Found ${zipsToProcess.length} ZIP codes to process\n`);

  const results: Array<{
    zipCode: string;
    success: boolean;
    county?: string;
    state?: string;
    error?: string;
  }> = [];

  // Process each ZIP
  for (const zipCode of zipsToProcess) {
    const lookup = await lookupZipCounty(zipCode);
    
    if (!lookup) {
      console.log(`    ${zipCode}: Need web lookup (not in training data)`);
      results.push({
        zipCode,
        success: false,
        error: 'Not in training data - needs web lookup'
      });
      continue;
    }

    try {
      // Insert into database
      await execute(`
        INSERT INTO zip_county_mapping (zip_code, county_name, state_code, state_name)
        VALUES ($1, $2, $3, $4)
        ON CONFLICT (zip_code, county_name, state_code) DO UPDATE SET
          state_name = EXCLUDED.state_name
      `, [
        zipCode,
        lookup.county,
        lookup.state,
        lookup.stateName
      ]);

      console.log(`   ${zipCode}: ${lookup.county}, ${lookup.state}`);
      results.push({
        zipCode,
        success: true,
        county: lookup.county,
        state: lookup.state
      });
    } catch (error: any) {
      console.log(`   ${zipCode}: Error - ${error.message}`);
      results.push({
        zipCode,
        success: false,
        error: error.message
      });
    }
  }

  // Remove processed ZIPs from file
  const successfulZips = new Set(results.filter(r => r.success).map(r => r.zipCode));
  const remainingLines = [header, ...dataLines.filter(line => {
    const zip = line.split('\t')[0]?.trim();
    return !zip || !successfulZips.has(zip);
  })];

  writeFileSync(filePath, remainingLines.join('\n') + '\n');

  // Summary
  console.log('\n=== Summary ===');
  console.log(`Total processed: ${results.length}`);
  console.log(`Successful: ${results.filter(r => r.success).length}`);
  console.log(`Failed: ${results.filter(r => !r.success).length}`);
  console.log(`Remaining in file: ${remainingLines.length - 1}`);
  console.log('\n');
}

