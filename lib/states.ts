export const STATE_CODE_SET = new Set<string>(STATES.map((s) => s.code));

export function normalizeStateQuery(input: string): string {
  return input
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

export function stripTrailingStateWord(input: string): string {
  const s = normalizeStateQuery(input);
  return s.replace(/\bstate\b$/i, '').trim();
}

export function findStateMatches(input: string, limit = 8): StateInfo[] {
  const raw = stripTrailingStateWord(input);
  if (!raw) return [];

  // Exact code match
  const maybeCode = raw.replace(/\s+/g, '').toUpperCase();
  if (/^[A-Z]{2}$/.test(maybeCode) && STATE_CODE_SET.has(maybeCode)) {
    const hit = STATES.find((s) => s.code === maybeCode);
    return hit ? [hit] : [];
  }

  // Name match (prefix > substring)
  const scored = STATES.map((s) => {
    const name = s.name.toLowerCase();
    const q = raw;
    let score = 0;
    if (name === q) score = 100;
    else if (name.startsWith(q)) score = 80;
    else if (name.includes(q)) score = 60;
    else if (q.length >= 3 && s.code.toLowerCase().startsWith(q)) score = 70;
    return { s, score };
  }).filter((x) => x.score > 0);

  scored.sort((a, b) => b.score - a.score || a.s.name.localeCompare(b.s.name));
  return scored.slice(0, limit).map((x) => x.s);
}


